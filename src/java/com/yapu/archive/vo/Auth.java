package com.yapu.archive.vo;

public class Auth {

	private Boolean filescan = false;
	private Boolean filedown = false;
	private Boolean fileprint = false;
	
	public void auth() {
	}
	
	public Boolean getFilescan() {
		return filescan;
	}
	public void setFilescan(Boolean filescan) {
		this.filescan = filescan;
	}
	public Boolean getFiledown() {
		return filedown;
	}
	public void setFiledown(Boolean filedown) {
		this.filedown = filedown;
	}
	public Boolean getFileprint() {
		return fileprint;
	}
	public void setFileprint(Boolean fileprint) {
		this.fileprint = fileprint;
	}
	
	
}
